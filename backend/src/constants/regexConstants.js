/**
 * Padrões de expessão regular
 */
module.exports = {
  email:
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  password: /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z]).{8,256}$/,
  phone:
    /^(?:(?:\+|00)?(55)\s?)?(?:\(?([1-9][0-9])\)?\s?)?(?:((?:9\d|[2-9])\d{3})-?(\d{4}))$/,
  fullname: /^[a-zA-Zà-úÁ-Ú]+(([',. -][a-zA-Zà-úÁ-Ú])?[a-zA-Zà-úÁ-Ú]*)*$/,
  cpfOrCnpj: /^(\d{11}|\d{14})$/,
  cep: /^\d{8}$/,
  uf: /^[A-Z]{2}$/,
  city: /^[a-zA-Zà-úÁ-Ú]+(([',. -][a-zA-Zà-úÁ-Ú])?[a-zA-Zà-úÁ-Ú]*)*$/,
};
